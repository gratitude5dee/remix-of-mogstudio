export const getModelsByType = (type: StudioModelMediaType): StudioModel[] => {
  switch (type) {
    case 'image':
      return IMAGE_MODELS;
    case 'video':
      return VIDEO_MODELS;
    case 'audio':
      return AUDIO_MODELS;
    case 'text':
      return TEXT_MODELS;
    default:
      return [];
  }
};

export const getModelsByTypeAndGroup = (
  type: StudioModelMediaType,
  uiGroup: StudioModelUiGroup
): StudioModel[] => getModelsByType(type).filter((model) => model.uiGroup === uiGroup);

